from sklearn.metrics import accuracy_score, precision_score, recall_score, f1_score, confusion_matrix, roc_curve, auc
import matplotlib.pyplot as plt
import seaborn as sns
import numpy as np
from utils import save_plot
import pandas as pd

def evaluate_model_performance(model_name, y_test, y_pred, y_prob=None):
    """Calculates and prints performance metrics."""
    acc = accuracy_score(y_test, y_pred)
    prec = precision_score(y_test, y_pred)
    rec = recall_score(y_test, y_pred)
    f1 = f1_score(y_test, y_pred)
    
    print(f"\n--- Model Performance: {model_name} ---")
    print(f"Accuracy:  {acc:.4f}")
    print(f"Precision: {prec:.4f}")
    print(f"Recall:    {rec:.4f}")
    print(f"F1 Score:  {f1:.4f}\n")
    return acc, prec, rec, f1

def plot_confusion_matrix(model_name, y_test, y_pred):
    """Plots and saves the confusion matrix heatmap."""
    cm = confusion_matrix(y_test, y_pred)
    fig, ax = plt.subplots(figsize=(6, 5))
    sns.heatmap(cm, annot=True, fmt='d', cmap='Blues', ax=ax,
                xticklabels=['Not Survived', 'Survived'], 
                yticklabels=['Not Survived', 'Survived'])
    ax.set_title(f'Confusion Matrix: {model_name}')
    ax.set_xlabel('Predicted Label')
    ax.set_ylabel('True Label')
    save_plot(fig, f'cm_{model_name.replace(" ", "_")}.png')

def plot_roc_curve(model_details):
    """Overlaps multiple ROC curves into a single comprehensive chart."""
    fig, ax = plt.subplots(figsize=(8, 6))
    
    for name, data in model_details.items():
        if data['y_prob'] is not None:
            fpr, tpr, _ = roc_curve(data['y_test'], data['y_prob'])
            roc_auc = auc(fpr, tpr)
            ax.plot(fpr, tpr, label=f'{name} (AUC = {roc_auc:.3f})')
            
    ax.plot([0, 1], [0, 1], color='navy', linestyle='--')
    ax.set_xlim([0.0, 1.0])
    ax.set_ylim([0.0, 1.05])
    ax.set_xlabel('False Positive Rate')
    ax.set_ylabel('True Positive Rate')
    ax.set_title('Receiver Operating Characteristic (ROC) Comparison')
    ax.legend(loc="lower right")
    save_plot(fig, 'roc_curve_comparison.png')

def plot_feature_importance(best_pipeline, preprocessor, model_name):
    """Extracts feature importances safely and creates an impressive bar chart."""
    try:
        classifier = best_pipeline.named_steps['classifier']
        
        if not hasattr(classifier, 'feature_importances_'):
            print(f"[{model_name}] does not support feature importance calculation.")
            return
            
        importances = classifier.feature_importances_
        
        # We fetch explicitly named output features mathematically generated by ColumnTransformer OneHotEncoder
        num_features = preprocessor.transformers_[0][2]
        cat_transformer = preprocessor.transformers_[1][1].named_steps['onehot']
        cat_features = preprocessor.transformers_[1][2]
        
        cat_feature_names = cat_transformer.get_feature_names_out(cat_features)
        all_features = np.concatenate([num_features, cat_feature_names])
        
        # Retrieve the Top 15 importances and reverse for better plot rendering (most important top)
        indices = np.argsort(importances)[-15:]
        
        fig, ax = plt.subplots(figsize=(10, 8))
        sns.barplot(x=importances[indices], y=[all_features[i] for i in indices], palette="magma", ax=ax, orient='h')
        ax.set_title(f'Top 15 Feature Importances: {model_name}')
        ax.set_xlabel('Relative Importance (Gini importance)')
        save_plot(fig, f'feature_importance_{model_name.replace(" ", "_")}.png')
        
    except Exception as e:
        print(f"Warning: Could not plot feature importances precisely. Error details: {e}")

def plot_model_comparison(results_df):
    """Produces the final bar chart to evaluate how models stack against one another."""
    fig, ax = plt.subplots(figsize=(10, 6))
    sns.barplot(x='Model', y='Accuracy', data=results_df, palette='Set2', ax=ax)
    ax.set_title('Final Model Accuracy Comparison')
    ax.set_ylim(0, 1.0)
    
    # Annotate explicitly with floating text values above each bar
    for i, v in enumerate(results_df['Accuracy']):
        ax.text(i, v + 0.02, f'{v:.4f}', ha='center', fontweight='bold')
        
    save_plot(fig, 'model_comparison_chart.png')
